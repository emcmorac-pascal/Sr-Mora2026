let tipoActual = "";
let indiceActual = 0;

function iniciarPaginaMisterios(tipo) {
  if (!rosarioData[tipo]) return;

  tipoActual = tipo;
  indiceActual = 0;

  const grupo = rosarioData[tipo];

  const tituloGeneral = document.getElementById("tituloGeneral");
  const subtituloGeneral = document.getElementById("subtituloGeneral");

  if (tituloGeneral) tituloGeneral.textContent = grupo.tituloGeneral;
  if (subtituloGeneral) subtituloGeneral.textContent = grupo.subtitulo;

  renderQuickButtons(grupo.misterios);
  mostrarMisterio();
}

function mostrarMisterio() {
  const grupo = rosarioData[tipoActual];
  if (!grupo) return;

  const misterio = grupo.misterios[indiceActual];

  const numero = document.getElementById("numeroMisterio");
  const titulo = document.getElementById("tituloMisterio");
  const descripcion = document.getElementById("descripcionMisterio");
  const fruto = document.getElementById("frutoMisterio");
  const contador = document.getElementById("contadorMisterio");
  const progressBar = document.getElementById("barraProgreso");
  const display = document.getElementById("mysteryDisplay");

  if (display) {
    display.classList.remove("fade-in");
    void display.offsetWidth;
    display.classList.add("fade-in");
  }

  if (numero) numero.textContent = misterio.numero;
  if (titulo) titulo.textContent = misterio.titulo;
  if (descripcion) descripcion.textContent = misterio.descripcion;
  if (fruto) fruto.textContent = "Fruto: " + misterio.fruto;
  if (contador) contador.textContent = `Misterio ${indiceActual + 1} de ${grupo.misterios.length}`;

  if (progressBar) {
    const porcentaje = ((indiceActual + 1) / grupo.misterios.length) * 100;
    progressBar.style.width = `${porcentaje}%`;
    progressBar.setAttribute("aria-valuenow", porcentaje.toFixed(0));
  }
}

function siguienteMisterio() {
  const grupo = rosarioData[tipoActual];
  if (!grupo) return;

  indiceActual++;

 if (indiceActual >= grupo.misterios.length) {
  window.location.href = "oraciones-despues-misterios.html";
  return;
}

  mostrarMisterio();
}

function anteriorMisterio() {
  const grupo = rosarioData[tipoActual];
  if (!grupo) return;

  indiceActual = (indiceActual - 1 + grupo.misterios.length) % grupo.misterios.length;
  mostrarMisterio();
}

function irAMisterio(index) {
  const grupo = rosarioData[tipoActual];
  if (!grupo) return;
  if (index < 0 || index >= grupo.misterios.length) return;

  indiceActual = index;
  mostrarMisterio();
}

function renderQuickButtons(misterios) {
  const contenedor = document.getElementById("quickButtons");
  if (!contenedor) return;

  contenedor.innerHTML = "";

  misterios.forEach((misterio, index) => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.textContent = index + 1;
    btn.title = misterio.titulo;
    btn.addEventListener("click", () => irAMisterio(index));
    contenedor.appendChild(btn);
  });
}

document.addEventListener("keydown", function (e) {
  if (!tipoActual) return;

  if (e.key === "ArrowRight") {
    siguienteMisterio();
  }

  if (e.key === "ArrowLeft") {
    anteriorMisterio();
  }
});